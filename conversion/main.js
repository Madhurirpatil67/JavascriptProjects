//Coversion

// Number
let num1=10;
console.log(String(num1)); // 10
console.log(Boolean(num1)); //true

let num2=0;
console.log(String(num2)); // 0
console.log(Boolean(num2)); //false

let num3=0.5;
console.log(String(num3)); // 0.5
console.log(Boolean(num3)); //true

let num4=-20;
console.log(String(num4)); // -20
console.log(Boolean(num4)); //true

